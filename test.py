from muratlib import add, subtract, multiply, divide, power

def main():
    print("57 + 12 = ", add(5, 7))
    print("5 - 211 = ", subtract(10, 2))
    print("14 * 4 = ", multiply(3, 4))
    print("12 / 2 = " , divide(16, 2))
    print("2 power 8 = ", power(2, 8))

if __name__ == "__main__":
    main()
