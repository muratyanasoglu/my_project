from muratlib import add, subtract,multiply, divide, power

def main():
    print("57 + 8 = ", add(57,8))
    print(("12 - 8 ="), subtract(12,8))
    print(("13 * 3 = ") , multiply (13, 3))
    print(("5 / 5 = ") , divide(5,5))
    print(("2 ^ 3 = ") , power(2,3))

if __name__ == "__main__":
    main()
